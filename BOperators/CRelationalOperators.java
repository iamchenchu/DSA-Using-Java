// Relational Operators : ==, !=, >, <, >=, <=  

public class CRelationalOperators {

    public static void main(String[] args) {
    int a = 10;
    int b = 20;
    int c = 10;

    System.out.println(a==c);
    System.out.println(a==b);
    System.out.println(b>a);
    System.out.println(b<a);
    System.out.println(b>=a);
    System.out.println(c>=a);
    System.out.println(c<=a);


    }


    
}
